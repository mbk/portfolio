package net.vrijheid.upgrade.demos.npe;

public class Address {
    private String street;
    private String city;

    public Address(String street, String city) {
        this.street = street;
        this.city = city;
    }

    //Note that below methods are never used, we just put them in to have decent
    //getters and setters, as you'd expect from such a "data" class
    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
